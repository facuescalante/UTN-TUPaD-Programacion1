""" 4. Crear dos funciones: calcular_area_circulo(radio) que reciba el ra
dio como parámetro y devuelva el área del círculo. calcular_peri
metro_circulo(radio) que reciba el radio como parámetro y devuel
va el perímetro del círculo. Solicitar el radio al usuario y llamar am
bas funciones para mostrar los resultados.
"""

def calcular_area_circulo(radio):                      # defino la función para calcular el área
    area = 3.14159 * radio**2                          # calculo el área usando la fórmula π * r²
    return area                                        # retorno el valor del área

def calcular_perimetro_circulo(radio):                 # defino la función para calcular el perímetro
    perimetro = 2 * 3.14159 * radio                    # calculo el perímetro usando la fórmula 2 * π * r
    return perimetro                                   # retorno el valor del perímetro

radio_valido = False                                   # inicializo la variable para validar el radio

while not radio_valido:                                # valido que el radio ingresado sea correcto
    radio = float(input("Ingrese el radio del círculo: "))  # solicito al usuario el valor del radio

    if radio > 0:                                      # valido que el radio sea mayor a cero
        radio_valido = True                            # confirmo que el radio es válido
    else:
        print("El radio debe ser mayor a cero.")       # informo que el radio no es válido

area = calcular_area_circulo(radio)                    # llamo a la función para calcular el área
perimetro = calcular_perimetro_circulo(radio)          # llamo a la función para calcular el perímetro

print(f"El área del círculo es: {area:.2f}")           # muestro el área con 2 decimales
print(f"El perímetro del círculo es: {perimetro:.2f}") # muestro el perímetro con 2 decimales


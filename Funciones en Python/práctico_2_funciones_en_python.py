# valido que la entrada sea numérica
while True:
    entrada = input("Ingrese la temperatura en grados Celsius: ")  # solicito la temperatura al usuario
    if entrada.replace('.', '', 1).replace('-', '', 1).isdigit():  # valido que sea número (permite negativos y decimales)
        temperatura_celsius = float(entrada)                       # convierto a número decimal
        break                                                      # salgo del bucle si es válida
    else:
        print("Entrada inválida. Ingrese solo números.")           # muestro mensaje si no es válida

temperatura_fahrenheit = celsius_a_fahrenheit(temperatura_celsius)  # llamo a la función para convertir

print(f"{temperatura_celsius:.2f} °C equivalen a {temperatura_fahrenheit:.2f} °F.")  # muestro el resultado con 2 decimales


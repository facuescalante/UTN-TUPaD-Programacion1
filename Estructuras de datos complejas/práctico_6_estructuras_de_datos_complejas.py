"""9) Cre√° una agenda donde las claves sean tuplas de (d√≠a, hora) y los valores sean eventos.

Permit√≠ consultar qu√© actividad hay en cierto d√≠a y hora.
"""

import unicodedata                                                           # Importo la librer√≠a para poder quitar acentos

def normalizar(texto):                                                      # Defino una funci√≥n para normalizar el texto
    texto = unicodedata.normalize('NFKD', texto)                            # Aplico la normalizaci√≥n Unicode
    texto = texto.encode('ASCII', 'ignore').decode('utf-8')                # Elimino los caracteres no ASCII (como acentos)
    return texto.lower()                                                   # Devuelvo el texto en min√∫sculas

agenda = {                                                                  # Creo un diccionario con eventos, usando tuplas (d√≠a, hora) como claves
    ("lunes", "10:00"): "Reuni√≥n de equipo",
    ("lunes", "15:30"): "Llamada con cliente",
    ("martes", "14:30"): "Clase de programaci√≥n",
    ("miercoles", "09:00"): "Consulta m√©dica",
    ("viernes", "18:00"): "Gimnasio",
    ("domingo", "9:30"): "Clase de golf"
}

dias_validos = ["lunes", "martes", "miercoles", "jueves",                  # Lista de d√≠as v√°lidos sin acento
                "viernes", "sabado", "domingo"]

while True:                                                                 # Bucle para validar el d√≠a ingresado
    dia_input = input("üìÖ Ingres√° el d√≠a que quer√©s consultar (ej. Lunes): ")
    dia_normalizado = normalizar(dia_input)                                 # Normalizo el texto ingresado
    if dia_normalizado in dias_validos:                                     # Verifico si es un d√≠a v√°lido
        break
    print("‚ö†Ô∏è D√≠a inv√°lido. Ingres√° un d√≠a de la semana correctamente.")

eventos_del_dia = [(hora, evento)                                          # Recorro la agenda y filtro los eventos del d√≠a ingresado
                   for (d, hora), evento in agenda.items() if d == dia_normalizado]

if eventos_del_dia:                                                        # Si hay eventos para ese d√≠a
    print(f"\nüìÜ Actividades para el d√≠a {dia_input.capitalize()}:")
    for hora, evento in sorted(eventos_del_dia):                           # Ordeno por hora y muestro cada evento
        print(f"üïí {hora} ‚Üí {evento}")
else:                                                                       # Si no hay eventos
    print(f"üì≠ No hay actividades programadas para el d√≠a {dia_input.capitalize()}.")

